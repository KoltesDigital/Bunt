function love.conf(t)
	t.title = "BUNT"            -- The title of the window the game is in (string)
	t.author = "Bloutiouf"      -- The author of the game (string)
	t.screen.width = 1280       -- The window width (number)
	t.screen.height = 800       -- The window height (number)
	t.screen.fullscreen = false  -- Enable fullscreen (boolean)
	t.screen.vsync = false      -- Enable vertical sync (boolean)
	t.screen.fsaa = 2           -- The number of FSAA-buffers (number)
end
